import SwiftUI

extension CGFloat {
    static let small: CGFloat = 5
    static let medium: CGFloat = 10
    static let large: CGFloat = 15
    static let extraLarge: CGFloat = 20
    static let cellHeightMin: CGFloat = 100
    static let cellHeightMax: CGFloat = 350
}

extension Double {
    static let cellBackgroundOpacity: CGFloat = 0.25
    static let cellBorderOpacity: CGFloat = 0.75
}

extension Int {
    static let nColumns = 2
    static let nTags = 10
    static let nModels = 50
}

extension TimeInterval {
    static let taskDuration: TimeInterval = 1.5
}
